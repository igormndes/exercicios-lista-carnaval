# Solicitando base e altura do triângulo:
b = float(input("Digite o comprimento da base do triângulo: "))

h = float(input("Digite o comprimento da altura do triângulo: "))


# Calculando a área do triângulo:

area = (b * h) / 2
print (f"A área do triângulo é: {area} m²\n")
